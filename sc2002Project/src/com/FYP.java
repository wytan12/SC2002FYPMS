package com;

/**
 * The FYP class is a subclass of Supervisor and provides a way to create supervisor objects with a specialized name and ID
 */
public class FYP extends Supervisor{
	/**
	 * Constructs a new FYP object with the given ID and name.
	 * @param id the ID of the FYP
	 * @param nm the name of the FYP
	*/
	public FYP(String id, String nm) {
		super(id, nm);
	}
}
